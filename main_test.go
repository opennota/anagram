func contains(buf *bytes.Buffer, needle string) bool {
	lines := strings.Split(buf.String(), "\n")
	for _, l := range lines {
		if l == needle {
			return true
		}
	}
	return false
}

func TestPrintAnagrams(t *testing.T) {
	var buf bytes.Buffer

	for _, tc := range testCases {
		for _, word := range tc {
			buf.Reset()
			printAnagrams(&buf, word, 1)
			for _, w := range tc {
				if w == word {
					continue
				}
				if !contains(&buf, w) {
					t.Errorf("%s must be among anagrams of %s", w, word)
				}
			}
		}
	}

	for _, tc := range testCases2 {
		buf.Reset()
		printAnagrams(&buf, tc.in, strings.Count(tc.out, " ")+1)
		if !contains(&buf, tc.out) {
			t.Errorf("%s must be among anagrams of %s", tc.out, tc.in)
		}
	}
}
